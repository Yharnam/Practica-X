    public class Sabor extends Ingredientes{

        
        public Sabor(String nombre){
            super(nombre);
            
        }
        
        
        @Override
        public int getTipo() {
            return 0;
        }
        public int compareTo(Sabor c1, Sabor c2){
            return c1.getNombre().compareTo(c2.getNombre());
        }
    }
    
    
    public class Cono extends Ingredientes{

        private double precio;
        
        public Cono(String nombre, double _precio){
            super(nombre);
            this.precio = _precio;
        }
        
        public double getPrecio(){
            return this.precio;
        }
        
        
        public void setPrecio(double _precio){
            this.precio = _precio;
        }
        
        @Override
        public int getTipo() {
            return 1;
        }
        
    }
    
    
    
    
    private void brn_conoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brn_conoActionPerformed
        if (!this.txt_Cono.getText().trim().isEmpty() && !this.txt_Precio.getText().trim().isEmpty()) {
            conoL.add(new Cono (txt_Cono.getText().trim(), Double.parseDouble(this.txt_Precio.getText())));          
            this.txt_Cono.setText(""); 
            this.txt_Precio.setText("");
            llenarTabla();
        }
    }//GEN-LAST:event_brn_conoActionPerformed

    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        if (!this.txt_sabor.getText().trim().isEmpty()) {
            
            saborL.add(new Sabor(this.txt_sabor.getText().trim()));

            int a = saborL.size();
            this.txt_sabor.setText("");
            llenarLista();
        }
    }//GEN-LAST:event_btn_agregarActionPerformed

    private void llenarTabla(){
        modelT = new DefaultTableModel();
        modelT.addColumn("Nombre");
        modelT.addColumn("Precio");
        for (int i = 0; i < conoL.size(); i++) {
        
        Object[] a = new Object[2];
        a[0] = this.txt_Cono.getText().trim();
        a[1] = Double.parseDouble(this.txt_Precio.getText());
        modelT.addRow(a);
        }
        
        tbCono.setModel(modelT);
        
    }
    
    private void llenarLista(){
        
        modelL = new DefaultListModel();
        
        for (int i = 0; i < saborL.size(); i++) {
            
            modelL.addElement(saborL.get(i).getNombre());
            
        }
        lSabor.setModel(modelL);
    } 
    
